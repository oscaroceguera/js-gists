const aPush = [1,2,3]
aPush.push(4)
console.log('Array Push', aPush)

const aPop = [1,2,3]
aPop.pop()
console.log('Array Pop', aPop)

const aShift = [1,2,3]
aShift.shift()
console.log('Array Shift', aShift)

const aUnShift = [1,2,3]
aUnShift.unshift(0)
console.log('Array Unshift', aUnShift)

const aConcat = ['a', 'b', 'c', 'd']
aConcat.concat('e')
console.log('Array Concat', aConcat)
